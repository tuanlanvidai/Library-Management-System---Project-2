    private void addMonthList() {
        Month month = Month.JANUARY;
        Boolean stop = false;
        cbxMonth.addItem("Chọn Tháng");
        while (!stop) {
            cbxMonth.addItem(month.toString());
            if (month.equals(Month.DECEMBER)) {
                stop = true;
            }
            month = month.plus(1);
        }
    }

